    private void tbContatosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbContatosMouseClicked
        botoes(true,false,false,true);
        int index = tbContatos.getSelectedRow();
        if(index>=0 && index<modelo.getRowCount()){
            String temp[] = modelo.getAluno(index);
            tfNome.setText(temp[0]);
            tfEmail.setText(temp[1]);
            tfTelefone.setText(temp[2]);
        }
    }//GEN-LAST:event_tbContatosMouseClicked

