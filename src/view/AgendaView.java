    private void loadTable(){
        modelo = new ContatoTM(controller.getContatos());
        tbContatos.setModel(modelo);
        
        tbContatos.getColumnModel().getColumn(0).setPreferredWidth(350);
        tbContatos.getColumnModel().getColumn(1).setPreferredWidth(100);
        tbContatos.getColumnModel().getColumn(1).setPreferredWidth(100);
    }
    
    private void loadComboBox(){
        ArrayList<String> grupos = controller.getGrupo();
        for(int i=0; i<grupos.size(); i++)
            cbGrupos.addItem(grupos.get(i));
    }
    
    private void loadTableGrupo(){
        
        try{
            modelo = new ContatoTM(controller.getContatos(cbGrupos.getSelectedItem().toString()));
            tbGrupos.setModel(modelo);   

        }catch(NullPointerException e){
            
        }
        tbGrupos.getColumnModel().getColumn(0).setPreferredWidth(350);
        tbGrupos.getColumnModel().getColumn(1).setPreferredWidth(100);
        tbGrupos.getColumnModel().getColumn(1).setPreferredWidth(100);

    }
    
    private void botoes(boolean N, boolean S, boolean C, boolean G){
        btNovo.setEnabled(N);
        btSalvar.setEnabled(S);
        btCancelar.setEnabled(C);
        btGrupos.setEnabled(G);
    }


